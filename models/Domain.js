const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const DomainSchema = new Schema({
  user: {
    type: Schema.Types.ObjectId,
    ref: 'users'
  },
  name: {
    type: String,
    required: true
  },
  description: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Domain = mongoose.model('domain', DomainSchema);